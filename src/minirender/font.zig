pub fn glyphPixel (char_code: u8, col: u3, row: u3) bool {
  if (char_code < 0x20 or char_code > 0x7E) return false;
  const idx = char_code - 0x20;
  return (font_data[idx][col] & (@as(u8, 1) << row)) != 0;
}

